"""
Linker script management for NexusLang bare-metal / freestanding builds.

This module handles:
  - Parsing and validating user-supplied linker scripts
  - Generating memory-region aware linker scripts from high-level descriptions
  - Invoking an external linker (ld, lld, arm-none-eabi-ld, etc.)
  - Producing build reports and symbol maps

This module is OPTIONAL and only used when building freestanding/bare-metal
firmware images.  It is not loaded during ordinary interpreter operation.
"""
from __future__ import annotations

import os
import re
import shutil
import subprocess
import tempfile
from dataclasses import dataclass, field
from typing import Any, Dict, List, Optional, Tuple


# ---------------------------------------------------------------------------
# Data structures
# ---------------------------------------------------------------------------

@dataclass
class MemoryRegion:
    """Describes a memory region for a linker script MEMORY block."""
    name: str
    origin: int
    length: int
    attributes: str = "rwx"  # r=read, w=write, x=execute, a=alloc, l=load

    @property
    def end(self) -> int:
        return self.origin + self.length

    def to_linker_script(self) -> str:
        attr = self.attributes.upper()
        return (f"    {self.name:<12} ({attr:<4}) : "
                f"ORIGIN = 0x{self.origin:08x}, "
                f"LENGTH = {self._fmt_length()}")

    def _fmt_length(self) -> str:
        if self.length % (1024 * 1024) == 0:
            return f"{self.length // (1024 * 1024)}M"
        if self.length % 1024 == 0:
            return f"{self.length // 1024}K"
        return str(self.length)


@dataclass
class LinkerSection:
    """Describes a section in the linker script SECTIONS block."""
    name: str
    load_region: str            # VMA region name
    run_region: Optional[str] = None   # LMA region name (for load-to-RAM copies)
    keep: bool = False
    patterns: List[str] = field(default_factory=lambda: ["*"])
    alignment: int = 4

    def to_linker_script(self, regions: Dict[str, MemoryRegion]) -> str:
        body_lines = []
        if self.alignment > 1:
            body_lines.append(f"        . = ALIGN({self.alignment});")
        if self.name == ".bss":
            body_lines.append("        __bss_start = .;")
        elif self.name == ".data":
            body_lines.append("        __data_load  = LOADADDR(.data);")
            body_lines.append("        __data_start = .;")

        for pat in self.patterns:
            inner = f"*({self.name}*)" if pat == "*" else f"{pat}({self.name}*)"
            if self.keep:
                inner = f"KEEP({inner})"
            body_lines.append(f"        {inner}")

        if self.name == ".bss":
            body_lines.append("        *(COMMON)")
            body_lines.append("        __bss_end = .;")
        elif self.name == ".data":
            body_lines.append("        __data_end = .;")

        at_clause = f" AT > {self.run_region}" if self.run_region else ""
        no_load = " (NOLOAD)" if self.name in (".bss", ".noinit") else ""
        body = "\n".join(body_lines)
        return (f"    {self.name}{no_load} : {{\n{body}\n    }}"
                f" > {self.load_region}{at_clause}")


@dataclass
class LinkerScriptConfig:
    """High-level description of the final linker script to be generated."""
    entry_symbol: str = "_start"
    regions: List[MemoryRegion] = field(default_factory=list)
    sections: List[LinkerSection] = field(default_factory=list)
    extra_symbols: Dict[str, str] = field(default_factory=dict)

    def add_region(self, name: str, origin: int, length: int,
                   attributes: str = "rwx") -> "LinkerScriptConfig":
        self.regions.append(MemoryRegion(name, origin, length, attributes))
        return self

    def add_section(self, name: str, load_region: str,
                    run_region: Optional[str] = None,
                    keep: bool = False,
                    alignment: int = 4) -> "LinkerScriptConfig":
        self.sections.append(LinkerSection(name, load_region, run_region, keep,
                                           alignment=alignment))
        return self

    def generate(self) -> str:
        """Generate the full linker script text from this configuration."""
        lines: List[str] = []
        lines.append(f"/* Generated by NexusLang bare-metal linker helper */")
        lines.append(f"ENTRY({self.entry_symbol})")
        lines.append("")
        lines.append("MEMORY")
        lines.append("{")
        regions_by_name = {}
        for r in self.regions:
            lines.append(r.to_linker_script())
            regions_by_name[r.name] = r
        lines.append("}")
        lines.append("")
        lines.append("SECTIONS")
        lines.append("{")
        for s in self.sections:
            lines.append(s.to_linker_script(regions_by_name))
        # Stack top symbol derived from last RAM region
        ram_regions = [r for r in self.regions if "w" in r.attributes.lower()]
        if ram_regions:
            last_ram = ram_regions[-1]
            lines.append(f"    __stack_top = ORIGIN({last_ram.name}) + LENGTH({last_ram.name});")
        # Extra user symbols
        for sym, expr in self.extra_symbols.items():
            lines.append(f"    {sym} = {expr};")
        lines.append("}")
        return "\n".join(lines) + "\n"


# ---------------------------------------------------------------------------
# Linker script presets
