def register_linker_functions(runtime: Any) -> None:
    """Register linker-related functions with the NexusLang runtime (for meta-programming)."""

    runtime.register_function("linker_generate_script",
        lambda *a: get_linker_script_for_arch(a[0] if a else "x86_64"))

    runtime.register_function("linker_cortex_m_config",
        lambda *a: cortex_m_config(
            int(a[0]) if len(a) > 0 else 0x08000000,
            int(a[1]) if len(a) > 1 else 256 * 1024,
            int(a[2]) if len(a) > 2 else 0x20000000,
            int(a[3]) if len(a) > 3 else 64 * 1024,
        ).generate())

    runtime.register_function("linker_x86_config",
        lambda *a: x86_flat_config(
            int(a[0]) if len(a) > 0 else 0x00100000,
            int(a[1]) if len(a) > 1 else 512 * 1024,
            int(a[2]) if len(a) > 2 else 0x00200000,
            int(a[3]) if len(a) > 3 else 256 * 1024,
        ).generate())

    runtime.register_function("linker_riscv_config",
        lambda *a: riscv_config(
            int(a[0]) if len(a) > 0 else 0x20010000,
            int(a[1]) if len(a) > 1 else 512 * 1024,
            int(a[2]) if len(a) > 2 else 0x80000000,
            int(a[3]) if len(a) > 3 else 16 * 1024,
        ).generate())

    runtime.register_function("linker_validate_script",
        lambda *a: LinkerScriptValidator.validate_file(a[0]) if a else (False, ["no path given"]))

    runtime.register_function("linker_invoke",
        lambda *a: vars(invoke_linker(
            list(a[0]),
            str(a[1]),
            a[2] if len(a) > 2 else None,
            a[3] if len(a) > 3 else None,
            a[4] if len(a) > 4 else None,
        )))
