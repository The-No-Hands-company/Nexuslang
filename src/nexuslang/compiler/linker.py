def cortex_m_config(flash_origin: int = 0x08000000,
                    flash_size: int = 256 * 1024,
                    sram_origin: int = 0x20000000,
                    sram_size: int = 64 * 1024) -> LinkerScriptConfig:
    """Return a pre-configured LinkerScriptConfig for ARM Cortex-M targets."""
    cfg = LinkerScriptConfig(entry_symbol="Reset_Handler")
    cfg.add_region("FLASH", flash_origin, flash_size, "rx")
    cfg.add_region("SRAM",  sram_origin,  sram_size,  "rwx")
    cfg.add_section(".vectors",  "FLASH", keep=True)
    cfg.add_section(".text",     "FLASH")
    cfg.add_section(".rodata",   "FLASH")
    cfg.add_section(".data",     "SRAM", run_region="FLASH")
    cfg.add_section(".bss",      "SRAM")
    return cfg


def x86_flat_config(rom_origin: int = 0x00100000,
                    rom_size: int = 512 * 1024,
                    ram_origin: int = 0x00200000,
                    ram_size: int = 256 * 1024) -> LinkerScriptConfig:
    """Return a pre-configured LinkerScriptConfig for x86 flat-binary targets."""
    cfg = LinkerScriptConfig(entry_symbol="_start")
    cfg.add_region("ROM", rom_origin, rom_size, "rx")
    cfg.add_region("RAM", ram_origin, ram_size, "rwx")
    cfg.add_section(".text",   "ROM")
    cfg.add_section(".rodata", "ROM")
    cfg.add_section(".data",   "RAM", run_region="ROM")
    cfg.add_section(".bss",    "RAM")
    return cfg


def riscv_config(flash_origin: int = 0x20010000,
                 flash_size: int = 512 * 1024,
                 sram_origin: int = 0x80000000,
                 sram_size: int = 16 * 1024) -> LinkerScriptConfig:
    """Return a pre-configured LinkerScriptConfig for RISC-V targets."""
    cfg = LinkerScriptConfig(entry_symbol="_start")
    cfg.add_region("FLASH", flash_origin, flash_size, "rx")
    cfg.add_region("SRAM",  sram_origin,  sram_size,  "rwx")
    cfg.add_section(".text.start", "FLASH", keep=True)
    cfg.add_section(".text",       "FLASH")
    cfg.add_section(".rodata",     "FLASH")
    cfg.add_section(".data",       "SRAM", run_region="FLASH")
    cfg.add_section(".bss",        "SRAM")
    return cfg


LINKER_PRESETS: Dict[str, Any] = {
    "cortex-m": cortex_m_config,
    "arm":      cortex_m_config,
    "x86":      x86_flat_config,
    "x86_64":   x86_flat_config,
    "riscv":    riscv_config,
    "riscv32":  riscv_config,
    "riscv64":  riscv_config,
}


def get_linker_script_for_arch(arch: str) -> str:
    """Return a generated linker script string for the given architecture."""
    key = arch.lower()
    if key not in LINKER_PRESETS:
        raise ValueError(
            f"linker: no preset for architecture '{arch}'. "
            f"Supported: {', '.join(sorted(LINKER_PRESETS))}"
        )
    cfg = LINKER_PRESETS[key]()
    return cfg.generate()


# ---------------------------------------------------------------------------
# Linker invocation
# ---------------------------------------------------------------------------

@dataclass
class LinkerInvocationResult:
    success: bool
    returncode: int
    stdout: str
    stderr: str
    output_file: Optional[str] = None
    map_file: Optional[str] = None


def find_linker(preferred: Optional[str] = None,
                arch: Optional[str] = None) -> Optional[str]:
    """Find a suitable linker binary on PATH.

    Looks for the preferred name first, then arch-prefixed linkers, then ld/lld.
    """
    candidates: List[str] = []
    if preferred:
        candidates.append(preferred)
    if arch:
        arch_lower = arch.lower()
        if "arm" in arch_lower or "cortex" in arch_lower:
            candidates += ["arm-none-eabi-ld", "arm-linux-gnueabi-ld"]
        elif "riscv" in arch_lower:
            candidates += ["riscv64-unknown-elf-ld", "riscv32-unknown-elf-ld"]
        elif "x86_64" in arch_lower:
            candidates += ["ld.lld", "ld"]
        else:
            candidates += ["ld.lld", "ld"]
    candidates += ["ld.lld", "ld"]

    for name in candidates:
        path = shutil.which(name)
        if path:
            return path
    return None


def invoke_linker(
    object_files: List[str],
    output_file: str,
    linker_script_path: Optional[str] = None,
    linker_binary: Optional[str] = None,
    arch: Optional[str] = None,
    extra_flags: Optional[List[str]] = None,
    generate_map: bool = True,
    verbose: bool = False,
) -> LinkerInvocationResult:
    """Invoke an external linker to produce a firmware binary.

    object_files     : list of .o / .a file paths to link
    output_file      : desired output path (e.g., firmware.elf)
    linker_script_path : path to the .ld linker script (optional)
    linker_binary    : explicit linker path; auto-detected if None
    arch             : architecture hint used for linker auto-detection
    extra_flags      : additional flags to pass to the linker
    generate_map     : emit a .map symbol file alongside the output
    verbose          : print the command before running

    Returns a LinkerInvocationResult with success status and output.
    """
    ld = linker_binary or find_linker(arch=arch)
    if ld is None:
        return LinkerInvocationResult(
            success=False, returncode=127, stdout="",
            stderr="linker: no linker binary found on PATH. "
                   "Install 'binutils' or pass --linker-binary."
        )

    cmd: List[str] = [ld]
    for obj in object_files:
        cmd.append(obj)
    cmd += ["-o", output_file]
    if linker_script_path:
        cmd += ["-T", linker_script_path]
    if generate_map:
        map_file = output_file + ".map"
        cmd += [f"--Map={map_file}"]
    else:
        map_file = None
    if extra_flags:
        cmd += extra_flags

    if verbose:
        print(" ".join(cmd))

    try:
        result = subprocess.run(cmd, capture_output=True, text=True)
        success = result.returncode == 0
        return LinkerInvocationResult(
            success=success,
            returncode=result.returncode,
            stdout=result.stdout,
            stderr=result.stderr,
            output_file=output_file if success else None,
            map_file=map_file if success and generate_map else None,
        )
    except FileNotFoundError as e:
        return LinkerInvocationResult(
            success=False, returncode=127, stdout="",
            stderr=f"linker: binary not found: {e}"
        )
    except Exception as e:
        return LinkerInvocationResult(
            success=False, returncode=1, stdout="",
            stderr=f"linker: unexpected error: {e}"
        )


# ---------------------------------------------------------------------------
# Linker script parser (validates user-supplied scripts)
# ---------------------------------------------------------------------------

class LinkerScriptValidator:
    """Performs basic sanity checks on a user-supplied linker script."""

    # Patterns we actively look for
    _RE_ENTRY   = re.compile(r"\bENTRY\s*\(", re.IGNORECASE)
    _RE_MEMORY  = re.compile(r"\bMEMORY\s*\{", re.IGNORECASE)
    _RE_SECTIONS = re.compile(r"\bSECTIONS\s*\{", re.IGNORECASE)
    _RE_ORIGIN  = re.compile(r"ORIGIN\s*=\s*0x[0-9a-fA-F]+")
    _RE_LENGTH  = re.compile(r"LENGTH\s*=\s*[\dKkMm]+")
    _RE_BSS_START = re.compile(r"__bss_start")
    _RE_BSS_END   = re.compile(r"__bss_end")

    @classmethod
    def validate(cls, script_text: str) -> Tuple[bool, List[str]]:
        """Validate linker script text.

        Returns (is_valid, list_of_warnings).
        """
        warnings: List[str] = []
        is_valid = True

        if not cls._RE_MEMORY.search(script_text):
            warnings.append("MEMORY block not found; memory regions will not be enforced.")
        if not cls._RE_SECTIONS.search(script_text):
            warnings.append("SECTIONS block not found; no output sections defined.")
            is_valid = False
        if not cls._RE_ENTRY.search(script_text):
            warnings.append("ENTRY symbol not defined; linker may default to _start.")
        if not cls._RE_ORIGIN.search(script_text):
            warnings.append("No ORIGIN directives found; memory addresses may be wrong.")
        if not cls._RE_BSS_START.search(script_text):
            warnings.append("__bss_start symbol missing; BSS zeroing may fail at startup.")
        if not cls._RE_BSS_END.search(script_text):
            warnings.append("__bss_end symbol missing; BSS zeroing may fail at startup.")

        return is_valid, warnings

    @classmethod
    def validate_file(cls, path: str) -> Tuple[bool, List[str]]:
        """Validate a linker script file at path."""
        with open(path) as f:
            text = f.read()
        return cls.validate(text)


# ---------------------------------------------------------------------------
# CLI helpers
